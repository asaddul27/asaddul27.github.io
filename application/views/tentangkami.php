						<p style="text-align:justify;">Maju Mundur High School didirikan pada tanggal 01 Januari 1993, dengan surat dari Kepala Kantor Wilayah Depdikbud Propinsi Amerika Tengah tanggal 22 Januari 1993, tentang persetujuan pendirian sekolah swasta yang diselenggarakan oleh Yayasan Subur Jaya dan diresmikan oleh Menteri Dalam Negeri Pochinki.
							<br />
							Kurikulum pertama yang dipergunakan adalah kurikulum 1921 dengan Rumpun Teknik mengarang berpipir bebas sakarepmu. Kurikulum ini dikembangkan mengacu pada Pendidikan Menengah dengan kemampuan umum setara SMA dan kemampuan kejuruan setara STM. Meskipun sekolah baru, input siswa relatif bagus karena rasio pendaftar dibanding yang diterima adalah 9 : 1. Selain itu, terdapat kurikulum khusus untuk siswa karyawan program 1 tahun dan 2 tahun yang berjumlah 3 angkatan. Pada tahun 1995-2000, diterapkan kurikulum 1994 yang pengembangannya masih mengacu kurikulum sebelumnya dengan tujuan membentuk alumni yang berotot SMK dan berotak SMA. Pada tahun 1995, input siswa mengalami booming yaitu rasio pendaftar dibanding yang diterima adalah 12 : 1.
							<br />
							Tahun berikutnya yaitu tahun 2000-2004, diterapkan kurikulum 1999 dengan menyesuaikan perubahan nama Jurusan Jaringan Kabel menjadi Program Keahlian Teknik Jaringan Akses Pelanggan. Selain itu, Maju Mundur High School juga mendirikan program keahlian baru (tahun 2000) yaitu Teknik Informatika yang merupakan program keahlian ketiga yang dibuka di Indonesia setelah SMK Telkom Malang dan Jakarta.<br />

							Mulai tahun 2004, pemerintah memberlakukan Kurikulum Berbasis Kompetensi (KBK) yaitu Kurikulum 2004, dan Maju Mundur High School juga memberlakukan kurikulum tersebut disertai pengembangannya. Pada kurikulum ini (tahun 2004), Teknik Informatika yang semula merupakan program keahlian dari Teknik Telekomunikasi sekarang berubah menjadi Bidang Keahlian dengan nama Teknologi Informasi dan Komunikasi yang memiliki empat program keahlian yaitu Teknik Komputer dan Jaringan, Rekayasa Perangkat Lunak, Multimedia, dan Teknik Penyiaran Radio. Sedangkan Program Keahlian Teknik Jaringan Akses Pelanggan terbagi menjadi dua program keahlian yaitu Teknik Akses Kabel dan Teknik Akses Radio. Pada saat yang bersamaan, Maju Mundur High School harus memilih program keahlian yang disesuaikan yaitu Program Keahlian Teknik Akses Radio dan Teknik Komputer dan Jaringan. Pada perkembangannya di tahun 2005, SMK Telkom Purwokerto menambah satu program keahlian lagi yaitu Rekayasa Perangkat Lunak.<br />
